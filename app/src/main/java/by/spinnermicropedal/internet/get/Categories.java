public class Categories {

    private String id;
    private String name;
    private String required_car;
    private String image;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRequired_car() {
        return required_car;
    }

    public void setRequired_car(String required_car) {
        this.required_car = required_car;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
